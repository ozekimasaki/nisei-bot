export function findDictionaryMatches(text: string): string[] {
  const normalized = text.trim();
  if (!normalized) return [];

  const matches = memoryDictionary.filter((word) => normalized.includes(word));
  return [...new Set(matches)].sort((left, right) => right.length - left.length);
}

export function findBestDictionaryMatch(text: string): string | null {
  return findDictionaryMatches(text)[0] ?? null;
}
